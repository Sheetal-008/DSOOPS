package com.Sheetal.Day_4;

public class Q1 {
    // number greater than 5
    public static void main(String[] args) {
        int i = 1;
        while(i<=10){
            if(i>5){
                System.out.println(i + ": High");
            }
            else{
                System.out.println(i + ": Low");
            }
            i++;
        }
    }
}
